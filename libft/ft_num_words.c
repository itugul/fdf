#include "libft.h"

int		ft_num_words(char const *s, char c)
{
	int i;
	int	j;

	i = -1;
	j = 0;
	while (s[++i])
	{
		if (s[i] != c && j >= 0)
		{
			j++;
			j *= -1;
		}
		if (s[i] == c && j < 0)
			j *= -1;
	}
	if (s[--i] != c)
		j *= -1;
	return (j);
}
